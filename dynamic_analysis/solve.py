objetivos_raw = [
    0x08070523, 0x04172d03, 0x5a4e1114, 0x05354056, 
    0x0211090e, 0x033b4c31, 0x340d0704, 0x11253032, 
    0x13202700, 0x5a02033b, 0x4f5e
]

objetivos = []
for v in objetivos_raw[:-1]:
    for i in range(4):
        objetivos.append((v >> (8 * i)) & 0xFF)

objetivos.append(objetivos_raw[-1] & 0xFF)
objetivos.append((objetivos_raw[-1] >> 8) & 0xFF)


flag = ""
for i in range(len(objetivos)):
    
    key = 0x61 ^ bytes_generados[i]

    flag += chr(objetivos[i] ^ key)

print(f"El flag es: {flag}")