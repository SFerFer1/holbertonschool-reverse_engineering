expected = [
    0x80, 0xe4, 0x08, 0x18, 0x4a, 0x58, 0xb8, 0xe4, 0xac, 0x34, 0x58, 0xe4, 0x7e, 0xbc, 0x9e, 0x8c, 
    0x7e, 0xd0, 0xc0, 0x7c, 0xac, 0xf4, 0x7e, 0x28, 0x9e, 0x04, 0x7e, 0xbc, 0x9e, 0x8c, 0x7e, 0x5c, 
    0x14, 0x4c, 0x7e, 0x5c, 0x7e, 0x6c, 0x02, 0x14, 0xb8, 0x4c, 0x14, 0xa4, 0x9e, 0x08, 0x7e, 0xe4, 
    0xf4, 0x08, 0x6a, 0x14, 0xa6, 0x5c, 0xb8, 0x7c, 0x9e, 0x28, 0x3e, 0xac
]

# Rango de caracteres: letras minúsculas, guion bajo y símbolos comunes
chars = "abcdefghijklmnopqrstuvwxyz_{}!?@#$%^&*() -+"

def check(char, pos):
    val = ord(char)
    if pos % 2 == 0:
        res = (val * -46) ^ -368 # -368 es 0xfffffe90 en 32 bits con signo
    else:
        res = (val * 316) ^ 0x9e0
    return (res & 0xff) == expected[pos]

flag = ""
for i in range(len(expected)):
    found = []
    for c in chars:
        if check(c, i):
            found.append(c)
    
    if len(found) == 1:
        flag += found[0]
    elif len(found) > 1:
        # Si hay colisión, marcamos con [ ] para elegir luego
        flag += f"[{''.join(found)}]"
    else:
        flag += "?"

print(f"Flag candidata: {flag}")
